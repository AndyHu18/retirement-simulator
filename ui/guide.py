def render_methodology():
    """計算邏輯說明（可展開）"""
    with st.expander("這個模擬器怎麼算的？（計算邏輯說明）"):
        st.markdown("""
### 模擬模型

把社區想像成**六個互相連通的水池**：

| 水池 | 代表什麼 | 水流入 | 水流出 |
|------|---------|--------|--------|
| 住戶池 | 住了多少人 | 新住戶入住 | 退住、過世、轉介護 |
| 資金池 | 口袋裡有多少錢 | 押金、月費、其他收入 | 營運成本、退費、建設 |
| 品牌信任 | 外界對社區的信心 | 入住率高、信託保護 | 時間衰減、負面事件 |
| 年齡結構 | 住戶的年齡組成 | 年輕新住戶 | 老化、退出 |
| 營運能力 | 團隊服務水準 | 經驗累積 | 人員流失 |
| 外部環境 | 經濟和社會氛圍 | 文化接受度提升 | 經濟衰退 |

這六個水池**互相影響**，形成連鎖反應。例如：

> 入住率高 → 口碑好 → 品牌信任上升 → 更多人想來 → 入住率更高

> 入住率低 → 退費壓力大 → 資金不夠退 → 信心崩潰 → 更多人退住

每 **3 個月**（一個季度）計算一次所有水池的進出量，共計算 **100 次 = 25 年**。

---

### 蒙地卡羅模擬

現實世界充滿不確定性 — 你無法預知經濟會不會衰退、入住速度有多快。

所以我們**不只跑一次**，而是跑 **1,000 次甚至 10,000 次**。

每次模擬時，系統會隨機調整以下參數：

| 參數 | 波動幅度 | 原因 |
|------|---------|------|
| 月費收入 | ±5% | 可能打折促銷或漲價 |
| 人員配比 | ±10% | 實際招聘與規劃可能有差距 |
| 入住轉化率 | ±20% | 市場反應難以精確預測 |
| 保險綁定效果 | ±40% | 台灣市場未驗證，不確定性最大 |
| 文化接受度 | ±20% | 社會觀念變化速度不確定 |

跑完後，我們看 1,000 個結果的**分布**：
- **最好的 5%** 和 **最差的 5%** → 灰色帶
- **中間 50%** → 藍色帶
- **最可能的結果** → 藍色線

這就像**擲骰子 1,000 次**，看哪個數字出現最多 — 你就知道什麼結果最可能、什麼結果雖然不太可能但也要防範。

---

### 三個分頁的差別

| 分頁 | 做什麼 | 什麼時候用 |
|------|--------|-----------|
| **單次模擬** | 用你設定的參數跑一次，看確定性結果 | 快速看趨勢走向 |
| **蒙地卡羅分布** | 跑上千次，顯示可能範圍 | 看最好/最壞情況 |
| **敏感度分析** | 分析哪個參數影響最大 | 決定優先處理什麼 |

後兩個分頁需要先按「開始蒙地卡羅模擬」按鈕才會有內容。
""")


