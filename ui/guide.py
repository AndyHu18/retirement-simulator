def render_glossary():
    """術語表（點擊跳出解釋）"""
    with st.expander("看不懂某個名詞？（術語表）"):
        st.caption("點擊任何一個詞，就會跳出白話文解釋")

        terms = list(GLOSSARY.keys())
        cols = st.columns(4)
        for i, term in enumerate(terms):
            with cols[i % 4]:
                with st.popover(f"**{term}**"):
                    st.markdown(GLOSSARY[term])


